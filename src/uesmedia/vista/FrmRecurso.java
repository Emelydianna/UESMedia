        private void cargarModelos(){

            cbModelo.removeAllItems();

            int indiceMarca =
                    cbMarca.getSelectedIndex();

            if(indiceMarca == -1){
                return;
            }

            Marca marca =
                    listaMarcas.get(indiceMarca);

            listaModelos =
                    modeloController.listarPorMarca(
                            marca.getId()
                    );

            for(Modelo modelo : listaModelos){

                cbModelo.addItem(
                        modelo.getNombre()
                );

            }

        }
