    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {

           if(cbModelo.getSelectedIndex() == -1){
               JOptionPane.showMessageDialog(
                       this,
                       "Seleccione un modelo"
               );
               return;
           }

           Modelo modelo =
                   listaModelos.get(
                           cbModelo.getSelectedIndex()
                   );

           int modeloId =
                   modelo.getId();

           RecursoController rc =
                   new RecursoController();

           boolean ok = rc.guardar(
                   modeloId,
                   txtCodigo.getText(),
                   txtNombre.getText(),
                   cbTipo.getSelectedItem().toString(),
                   txtDescripcion.getText(),
                   cbEstado.getSelectedItem().toString(),
                   chkMantenimiento.isSelected(),
                   new java.sql.Date(System.currentTimeMillis())
           );

           if(ok){

               JOptionPane.showMessageDialog(
                       this,
                       "Recurso registrado correctamente"
               );

               listar();
               limpiar();

           }else{

               JOptionPane.showMessageDialog(
                       this,
                       "Error al registrar recurso"
               );

           }

       }catch(Exception e){

           JOptionPane.showMessageDialog(
                   this,
                   e.getMessage()
           );

       }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         if(txtId.getText().isEmpty()){

            JOptionPane.showMessageDialog(
                    this,
                    "Seleccione un recurso"
            );

            return;
        }

        int opcion =
                JOptionPane.showConfirmDialog(
                        this,
                        "¿Desea eliminar este recurso?"
                );

        if(opcion == JOptionPane.YES_OPTION){

            RecursoController rc =
                    new RecursoController();

            boolean ok =
                    rc.eliminar(
                            Integer.parseInt(
                                    txtId.getText()
                            )
                    );

            if(ok){

                JOptionPane.showMessageDialog(
                        this,
                        "Recurso eliminado"
                );

                listar();
                limpiar();

            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

